        $display("[ ERR] Failure in processor test case at #%0d", $time);
        $display("  pc real %d expected %d", nextPC, pc);
        $write("  expected: ");
        for (int i = 0; i < regs; i++) begin
            $write("    r%0d=%0d ", i, arr[8*i +: 8]);
